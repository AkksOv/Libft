char	*ft_strchr(const char *s, int c)
{
	char	*p;
	char	tc;

	tc = (char) c;
	p = (char *)s;
	while (*p != tc && *p)
		++p;
	if (*p == tc)
		return (p);
	else
		return (0);
}
